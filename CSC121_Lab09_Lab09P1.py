import turtle
turtle.setup(800, 600)
turtle1 = turtle.Turtle()
turtle1.hideturtle()
turtle1.setposition(100,0)
turtle1.penup()
turtle1.setposition(0,100)
turtle1.pendown()
turtle1.setposition(100,100)
turtle1.setposition(0,100)
turtle1.setposition(0,-100)
turtle1.setposition(100,-100)
turtle.exitonclick()
